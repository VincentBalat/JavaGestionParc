    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
        
        String msn = null;
        String mnom = null;
        String mtype = null;
        String mmarque = null;
        String mmodele = null;
        Salle msalle = null ;
        boolean mactif = true;
        msn = String.valueOf(numserie.getText());
        mnom = nom.getText();
        mtype = type.getText();
        mmarque = marque.getText();
        mmodele = modele.getText();
        
        for(Salle s : mainInt.getController().listAllSalles()){
            if(s.toString().equals(salle.getSelectedItem()))
                msalle = s;
        }
        if(act.isSelected()== false)
            mactif = false;   
        lMessage.setText("");
        try {
            mainInt.getController().modifyEquipement(msn, msalle,mnom, mmarque, mmodele, mactif, mtype);
        } catch (SQLException | ClassNotFoundException ex) {
             ErreurBD err = new ErreurBD();
             err.setVisible(true);
        }
        mainInt.UpdateJTableEq(msalle);
        this.setVisible(false);
    }//GEN-LAST:event_modifierActionPerformed

